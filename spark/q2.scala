// Query C: Stream-static join benchmark.
// Reads from Kafka, joins each row against a static lookup table (generated by utils/static.scala)
// using a broadcast join, base64-encodes the payload, and writes back to Kafka.
// Requires running utils/static.scala first to generate the lookup table.

import org.apache.spark.sql.execution.streaming.RealTimeTrigger
import org.apache.spark.sql.functions._
import org.apache.spark.sql.expressions.scalalang._
import org.apache.spark.sql.streaming.Trigger
import org.apache.spark.sql.Encoders
import org.apache.spark.sql.types._


val isStateful = false
val kafkaBootstrapServers = sys.env("KAFKA_BOOTSTRAP_SERVERS")
val inputTopic = sys.env("INPUT_TOPIC")
val outputTopic = sys.env("OUTPUT_TOPIC")
val checkpointLocation = sys.env("CHECKPOINT_LOCATION")


// Toggle between Real-Time Mode (continuous, lower latency) and micro-batch (ProcessingTime(0))
val rtm_trigger = RealTimeTrigger.apply()
val mtm_trigger = Trigger.ProcessingTime(0)

val trigger = rtm_trigger

spark.conf.set("spark.sql.shuffle.partitions", 40)
spark.conf.set("spark.sql.streaming.stateStore.rocksdb.trackTotalNumberOfRows", false)
spark.conf.set("spark.sql.streaming.stateStore.checkpointFormatVersion", 2)

val kafkaStream = spark.readStream
 .format("kafka")
 .option("kafka.bootstrap.servers", kafkaBootstrapServers)
 .option("subscribe", inputTopic)
 .option("kafka.fetch.max.wait.ms", "50")
 .option("kafka.max.partition.fetch.bytes", "10485760") // 10MB
 .load()
 .withColumn("value", substring(col("value"), 0, 500 * 1000))
 // Assign a random join key and broadcast-join against the static lookup table
 .withColumn("joinKey", (random() * lookupCardinality).cast(IntegerType))
 .join(broadcast(spark.read.format("parquet").load(staticTableLocation)), expr("joinKey = lookupKey"))
 .withColumn("value", base64(col("value")))
 // Propagate Kafka ingestion timestamp as a header for end-to-end latency measurement
 .withColumn(
   "headers",
   array(
     struct(
       lit("source-timestamp") as "key",
       unix_millis(col("timestamp")).cast("STRING").cast("BINARY") as "value")))
 .drop(col("timestamp"))


val q = kafkaStream.writeStream
 .trigger(trigger)
 .format("kafka")
 .option("kafka.bootstrap.servers", kafkaBootstrapServers)
 .option("topic", outputTopic)
 .option("checkpointLocation", checkpointLocation)
 .option("asyncProgressTrackingEnabled", true)
 .option("kafka.buffer.memory", "67108864")  // 64MB
 .option("kafka.compression.type", "snappy")
 .outputMode("update")
 .queryName("benchRTM")
 .start()
